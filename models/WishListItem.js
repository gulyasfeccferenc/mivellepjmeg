class WishListItem {
    constructor(id, name, detail, url, category, imageUrl, singleItem, color) {
        this.id = id;
        this.name = name;
        this.detail = detail;
        this.url = url;
        this.category = category;
        this.imageUrl = imageUrl;
        this.singleItem = singleItem;
        this.color = color;
    }
}

export default WishListItem
